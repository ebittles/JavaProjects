public class Commercial{
	private double t = 0;
	
	public Commercial(double demand, double w){
		if(demand<50){
			if(w<=100){
				t = w * 0.03929;
			}else if(w<=500&&w>100){
				t = 100*0.03929 + (w-100)*0.02729;
			}else{
				t = 100*0.03929 + 400*0.02729 + (w-500)*0.01729;
			}
		}else{
			if(w<=15000){
				t = w * 0.01129;
			}else if(w<=40000&&w>15000){
				t = 15000*0.01129 + (w-15000)*0.00929;
			}else if(w<=100000&&w>40000){
				t = 15000*0.01129 + 25000*0.00929 + (w-40000)*0.00699;
			}else if(w<=500000&&w>100000){
				t = 15000*0.01129 + 25000*0.00929 + 60000*0.00699 + (w-100000)*0.00589;
			}else{
				t = 15000*0.01129 + 25000*0.00929 + 60000*0.00699 + 400000*0.00589 + (w-500000)*0.00569;
			}
			
			t += demand*2.01;
			
		}
	}
	
	public double getTotal(){
		return t;
	}
}